                        <div id="collapseSeven" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSeven">
                            <div class="panel-body">
                                There are four main strategies to perform this; One-Way, Loop, Triangle, and Long-Short.
                                <ol><br>
                                    <li>One Way: This arbitrage strategy refers to the purchase of one particular cryptocurrency
                                        from a market of a lower purchase value and reselling it in another market with a
                                        higher sale value.</li><br>
                                    <li>Loop: Looping refers to two instances of one-way arbitrage, where the first one-way can
                                        be a profit while the second one-way can be marginally or no difference in value.
                                    </li><br>
                                    <li>Triangle: Similarly to the loop arbitrage, the triangle takes into account of three exchanges
                                        instead of two, while taking into account any transactional and platform fees. The
                                        pricing discrepancy between three exchanges will be calculated before conducting
                                        the loop.
                                    </li><br>
                                    <li>Long-Short (Blackbird): This Arbitraging strategy is developed by Massachusetts Institute
                                        of Technology (MIT). It employs a tool called Blackbird that can calculate differences
                                        between two markets for a certain cryptocurrency, and initiates either a long position
                                        or a short position.
                                    </li>
                                </ol>
